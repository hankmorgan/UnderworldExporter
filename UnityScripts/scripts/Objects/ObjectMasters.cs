    void InitObj(
        int index,
        int objClass,
        int objSubClass,
        int objSubClassIndex,
        int type,
        string desc,
        int WorldIndex,
        int InventoryIndex,
        bool isUseable,
        bool isMoveable,
        int startFrame,
        int useSprite)
    {
        objProp[index].index = index;
        objProp[index].objClass = objClass;  //For Shock
        objProp[index].objSubClass = objSubClass;
        objProp[index].objSubClassIndex = objSubClassIndex;
